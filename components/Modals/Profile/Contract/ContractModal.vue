            <span class="text-gray-800">تبصره 4: اگر روز پرداخت مصادف با پنجشنبه باشد، پرداخت در روز قبل یعنی چهارشنبه انجام خواهد شد. اگر روز پرداخت مصادف با جمعه یا تعطیلات رسمی باشد، پرداخت در روز کاری بعدی انجام خواهد شد.</span>
