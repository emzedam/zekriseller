        <span class="text-[12pt]">
          <span class="text-[11.0pt]">
            <span class="text-gray-800">7-28- فروشنده متعهد به معرفی نماینده خود جهت آموزش و توجیه در خصوص نحوه استفاده از درگاه اینترنتی می باشد. متعاقباً فروشنده با استفاده از پنل اعلام و اقرار می نماید که آموزش های لازم را فراگرفته و مسئولیت پنل را برعهده دارد و نمی تواند به ادعای ناآگاهی یا عدم اطلاع رسانی، ادعایی را علیه پتومن طرح نماید.</span>
