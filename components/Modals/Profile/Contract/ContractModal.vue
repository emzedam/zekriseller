            <span class="text-gray-800">8-5- وجوه ناشی از فروش کالا پس از کسر کمیسیون، هزینه ها و مطالبات قراردادی و تأیید واحد مالی پتومن، طبق زمان بندی مشخص شده در پنل سلر سنتر به فروشنده پرداخت می شود.</span>
