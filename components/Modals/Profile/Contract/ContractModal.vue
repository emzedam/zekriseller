          <span class="text-[11.0pt]">
            <span class="text-gray-800">8-2- طراحی درگاه اینترنتی، تعیین جایگاه کالا، نحوه معرفی، شیوه نمایش و تبلیغ کالا درصورت درخواست فروشنده در قبال دریافت هزینه های مربوطه به شرح جدول هزینه ها و جریمه ها مندرج در پنل فروشنده بر عهده پتومن خواهد بود.</span>
