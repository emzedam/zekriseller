          <span class="text-[11.0pt]">
            <span class="text-gray-800">• کالاهایی که در اثنای همکاری فروش آن ها ممنوع و غیر قانونی شده است،</span>
          </span>
        </span>
      </p>
      <p class="text-justify">
        <span class="text-[12pt]">
          <span class="text-[11.0pt]">
            <span class="text-gray-800">• یا کالاهایی که به هر علت با توجه به مفاد این قرارداد، موظف به استرداد آن می باشد،</span>
          </span>
        </span>
      </p>
      <p class="text-justify">
        <span class="text-[12pt]">
          <span class="text-[11.0pt]">
            <span class="text-gray-800">پتومن مسئولیتی جهت نگهداری این نوع کالاها نخواهد داشت و پتومن عدم استرداد کالاها ظرف مدت یاد شده را به منزله اعراض فروشنده از مالکیت کالاهای فوق الذکر تلقی نموده لذا به موجب امضای این قرارداد از سوی فروشنده، ضمن قبول مالکیت پتومن نسبت به آن کالاها، پتومن </span>
          </span>
          <span class="text-[11.0pt]">
            <span class="text-gray-800">مخیر است</span>
          </span>
          <span class="text-[11.0pt]">
            <span class="text-gray-800"> تا کالاهای باقی مانده را امحا نماید. در این صورت فروشنده هیچگونه ادعایی نسبت به کالا یا ارزش کالا اعراض شده نخواهد داشت. همچنین پتومن مستحق دریافت هزینه­های انبارداری و امحاء و سایر مطالبات خود بوده و می­تواند وجوه مربوطه را از محل سایر تضامین دریافت نماید. همچنین حق مطالبه قانونی این هزینه­ها از طریق مراجع ذی­صلاح به قوت خود باقی است. ادعای فروشنده مبنی بر عدم اطلاع از اخطار مسموع نبوده و ملاک، ارسال اخطار از سوی پتومن به طرق یاد شده خواهد بود.</span>
