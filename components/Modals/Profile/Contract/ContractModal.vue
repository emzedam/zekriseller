        <span class="text-[12pt]">
          <span class="text-[11.0pt]">
            <span class="text-gray-800">7-26- درصورت تکرار نقض تعهد به مدت سه بار، پتومن می تواند ضمن قطع دسترسی فروشنده به پنل حسب صلاحدید نسبت به فسخ قرارداد اقدام نماید. فسخ قرارداد مانع از مطالبه وجوه التزام و خسارات نخواهد شد.</span>
