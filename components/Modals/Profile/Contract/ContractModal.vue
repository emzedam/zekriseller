          </span>
          <span class="ltr text-[11.0pt]">
            <span class="text-gray-800">14</span>
          </span>
          <span class="text-[11.0pt]">
            <span class="text-gray-800"> روز از اخطار پتومن نسبت به استرداد کالاهای به شرح زیر اقدام ننماید:</span>
          </span>
        </span>
      </p>
      <p class="text-justify">
        <span class="text-[12pt]">
