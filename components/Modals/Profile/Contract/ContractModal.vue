        <span class="text-[12pt]">
          <span class="text-[11.0pt]">
            <span class="text-gray-800">8-4- پتومن اقدامات و هماهنگی لازم را جهت آموزش و توجیه فروشنده یا نمایندگان ایشان در خصوص نحوه استفاده از درگاه اینترنتی انجام خواهد داد و فروشنده متعهد به حضور در جلسات اجباری می باشد، در غیر این صورت پتومن می تواند با تشخیص انحصاری خود پنل فروشنده را به دلیل عدم حضور در جلسات آموزشی متوقف نماید و فروشنده حق هیچگونه اعتراضی در این خصوص نخواهد داشت. متعاقباً فروشنده با استفاده از پنل، اعلام و اقرار می­نماید که آموزش­های لازم را فراگرفته و مسئولیت پنل را برعهده دارد و نمی­تواند به ادعای ناآگاهی یا عدم اطلاع رسانی، ادعایی را علیه پتومن طرح نماید.</span>
