        <span class="text-[12pt]">
          <span class="text-[11.0pt]">
            <span class="text-gray-800">8-1- پتومن به عنوان واسطه فضای مجازی کسب و کار و خدمات دهنده در خصوص امور جانبی و امین در نگهداری وجوه پرداختی حاصل از فروش، همکاری لازم را با فروشنده در راستای عرضه کالاهای موضوع قرارداد در درگاه اینترنتی (معرفی، توصیف و عکس گذاری، ارسال آنها به مشتریان بالقوه و سایر خدمات به شرح مقرر در پنل فروشنده) با اخذ هزینه­های مربوط مبذول خواهد داشت.</span>
